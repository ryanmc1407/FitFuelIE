package com.example.fitfuelie.data.model

/**
 * TrainingFrequency Enum
 * 
 * Represents how often the user works out per week.
 * Used during onboarding to understand the user's activity level.
 * This helps personalize workout recommendations and recovery suggestions.
 */
enum class TrainingFrequency {
    TWO_THREE_DAYS,
    FOUR_FIVE_DAYS,
    SIX_PLUS_DAYS
}
